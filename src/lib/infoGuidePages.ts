export function getInfoGuidePage(path: string): InfoGuidePageData | undefined {
  return INFO_GUIDE_PAGES.find((page) => page.path === path);
}

export function buildInfoGuideMetadata(path: string): Metadata {
  const page = getInfoGuidePage(path);
  if (!page) {
    return buildPageMetadata({
      title: "가이드를 찾을 수 없어요",
      description: "요청하신 가이드 페이지를 찾을 수 없습니다.",
      path,
      noindex: true,
    });
  }

  return buildPageMetadata({
    title: page.title,
    description: page.description,
    path: page.path,
    keywords: [...page.keywords],
    ogType: "article",
  });
}
